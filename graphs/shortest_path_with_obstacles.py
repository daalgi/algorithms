def a_star(grid: List[List[int]], k: int) -> int:
    # Time complexity: O(N K log(N K))
    # Space complexity: O(N K)
    # where `N` is the number of cells
    # and `K` number of times we can visit a cell,
    # which equals the obstancle elimination quotas
    rows, cols = len(grid), len(grid[0])

    moves = ((1, 0), (0, 1), (-1, 0), (0, -1))

    # Hashset to keep track of the visited cells
    # for a given remaining `k`
    visited = set([0, 0, k])

    # Min-heap:
    # If we use `steps` as comparison variable, it could be not optimal.
    # If we use distance (remaining distance to the end), we could
    # get a path that's not the shortest.
    # Use as main variable a combination of the steps so far and the
    # Manhattan distance to the end: total_distance_estimation.
    # min-heap: (total_distance_estimation, steps, remaining_k, row, col)
    heap = [(rows + cols - 2, 0, k, 0, 0)]
    while heap:
        # steps, dist, curr_k, r, c = heapq.heappop(heap)
        dist_estimation, steps, remaining_k, r, c = heapq.heappop(heap)

        # if r == last_row and c == last_col:
        #     return steps

        # If the dist_estimationance to the end is less or equal
        # to the `remaining_k`,
        # there's no need to further explore other paths
        if dist_estimation - steps <= remaining_k:
            return dist_estimation

        # Explore the new neighboring cells
        nsteps = steps + 1
        for dr, dc in moves:
            nr, nc = r + dr, c + dc

            # Check if the new cell is out of boundaries
            if not 0 <= nr < rows or not 0 <= nc < cols:
                continue

            # Check if we still have `remaining_k` after visiting
            # the new cell, and that the new cell isn't in `visited`
            nremaining_k = remaining_k - grid[nr][nc]
            if nremaining_k < 0 or (nr, nc, nremaining_k) in visited:
                continue

            # Add the new cell as a candidate to belong
            # to the shortest path
            visited.add((nr, nc, nremaining_k))
            # ndist_estimation = manhattan_distance + nsteps
            ndist_estimation = rows - nr + cols - nc - 2 + nsteps
            heapq.heappush(heap, (ndist_estimation, nsteps, nremaining_k, nr, nc))

    return -1


